// WAP that includes variables, constants, comments. Declare different data types and display their values
#include <stdio.h>
#define PI 3.14
int main() 
{
    int age = 25;
    char latter = 'D'; 
    float height = 5.9;

    printf("Constant PI: %f\n", PI);

    printf("Age: %d\n", age);
    printf("Latter: %c\n", latter);
    printf("Height: %.1f\n", height);
}
