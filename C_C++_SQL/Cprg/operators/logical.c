// logical operator: and(&&),OR(||),not(!);

#include<stdio.h>
int main(){
    int num1,num2,num3;
    printf("Enter numbers:");
    scanf("%d %d",&num1,&num2);
    printf("\n Logical AND  =%d",num1>20 && num2>=50);
    printf("\n Logical OR=  %d",num1=10 || num2>0);

    printf("\n Logical not=%d",!num3);
    printf("\n Num1=%d",num3);
}